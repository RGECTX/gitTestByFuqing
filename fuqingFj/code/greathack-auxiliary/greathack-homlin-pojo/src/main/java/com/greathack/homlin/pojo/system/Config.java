package com.greathack.homlin.pojo.system;

/**
 * Config entity. @author MyEclipse Persistence Tools
 */

public class Config implements java.io.Serializable {

	// Fields

	/**
	 * 
	 */
	private static final long serialVersionUID = 6204403961312635927L;
	private String dataKey;
	private String data;
	private String remark;

	// Constructors

	/** default constructor */
	public Config() {
	}

	/** minimal constructor */
	public Config(String dataKey) {
		this.dataKey = dataKey;
	}

	/** full constructor */
	public Config(String dataKey, String data, String remark) {
		this.dataKey = dataKey;
		this.data = data;
		this.remark = remark;
	}

	// Property accessors

	public String getDataKey() {
		return this.dataKey;
	}

	public void setDataKey(String dataKey) {
		this.dataKey = dataKey;
	}

	public String getData() {
		return this.data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getRemark() {
		return this.remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}