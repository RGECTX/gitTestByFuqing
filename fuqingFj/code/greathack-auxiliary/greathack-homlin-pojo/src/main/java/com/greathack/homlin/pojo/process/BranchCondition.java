package com.greathack.homlin.pojo.process;

/**
 * 分支条件数据，传递数据的结构
 * @author greathack
 *
 */
public class BranchCondition {
	
	/**
	 * 当前节点ID
	 */
	private long processNodeId;
	
	/**
	 * 到达当前节点的时间
	 */
	private long fetchTime;
	
	/**
	 * 当前节点的审批状态：1：未到达，2：等待审批，4：审批通过，8：审批不通过
	 */
	private long nodeState;
	
	
	/**
	 * 分支条件
	 */
	private String condition;


	public long getProcessNodeId() {
		return processNodeId;
	}


	public void setProcessNodeId(long processNodeId) {
		this.processNodeId = processNodeId;
	}


	public long getFetchTime() {
		return fetchTime;
	}


	public void setFetchTime(long fetchTime) {
		this.fetchTime = fetchTime;
	}


	public long getNodeState() {
		return nodeState;
	}


	public void setNodeState(long nodeState) {
		this.nodeState = nodeState;
	}


	public String getCondition() {
		return condition;
	}


	public void setCondition(String condition) {
		this.condition = condition;
	}

}
