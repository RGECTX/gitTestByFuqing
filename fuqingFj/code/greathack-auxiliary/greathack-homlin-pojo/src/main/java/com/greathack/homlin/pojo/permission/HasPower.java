package com.greathack.homlin.pojo.permission;

import java.util.List;

public class HasPower {
	private Boolean hasPower;
	private List<PowerItem> powerItemList;
	public Boolean getHasPower() {
		return hasPower;
	}
	public void setHasPower(Boolean hasPower) {
		this.hasPower = hasPower;
	}

	public List<PowerItem> getPowerItemList() {
		return powerItemList;
	}

	public void setPowerItemList(List<PowerItem> powerItemList) {
		this.powerItemList = powerItemList;
	}
}
